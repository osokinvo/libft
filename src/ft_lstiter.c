#include "list.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst == (void *)0 || f == NULL)
		return ;
	while (lst)
	{
		(*f)(lst);
		lst = lst->next;
	}
}
